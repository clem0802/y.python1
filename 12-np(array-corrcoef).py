import numpy as np
E = np.array([[2, 4, 6], [1, 3, 5]])
print(np.std(E))  # 1.707825127659933
# mean = ((2+4+6+1+3+5) /6) = 3.5
# std = sqrt(((2-3.5)^2 + (4-3.5)^2 + (6-3.5)^2 + (1-3.5)^2 + (3-3.5)^2 + (5-3.5)^2) /6)
#     = sqrt(2.91)
#     = 1.707825127659933
print(np.corrcoef(E[:,0], E[:,1]))  # [[1. 1.] [1. 1.]]